// ─────────────────────────────────────────────────────────────────────
// Error model
// ─────────────────────────────────────────────────────────────────────

export type ActionRuntimeErrorCode =
  | 'BUDGET_EXCEEDED'
  | 'QUOTA_EXCEEDED'
  | 'KILL_SWITCH_OPEN'
  | 'PERSONA_NOT_BOUND'
  | 'AUTONOMY_CAP_EXCEEDED'
  | 'PRECONDITION_FAILED'
  | 'PLAN_EXPIRED'
  | 'IDEMPOTENT_REPLAY'
  | 'STEP_HANDLER_ERROR'
  | 'COMPENSATION_FAILED'
  | 'APPROVAL_BLOCKED'
  | 'INVALID_PLAN';

export class ActionRuntimeError extends Error {
  constructor(
    public readonly code: ActionRuntimeErrorCode,
    message: string,
    public readonly details?: Readonly<Record<string, unknown>>,
  ) {
    super(message);
    this.name = 'ActionRuntimeError';
  }
}
