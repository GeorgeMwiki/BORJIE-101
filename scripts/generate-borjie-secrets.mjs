#!/usr/bin/env node
/**
 * generate-borjie-secrets.mjs — standalone crypto-secret generator.
 *
 * Print or write to .env.local the seven app-owned secrets:
 *   ENCRYPTION_MASTER_KEY, JWT_SECRET, JWT_REFRESH_SECRET,
 *   SESSION_HASH_SECRET, MCP_API_KEY, INTERNAL_API_KEY, CRON_SECRET.
 *
 * Use this to rotate secrets WITHOUT touching Supabase. The rotation
 * runbook is at Docs/RUNBOOKS/supabase-bootstrap.md#rotating-just-secrets.
 *
 * Usage:
 *   pnpm gen-secrets                  # print to stdout (default)
 *   pnpm gen-secrets --write          # patch .env.local in place
 *   pnpm gen-secrets --write --force  # also overwrite already-populated values
 *   pnpm gen-secrets --json           # JSON output (for piping)
 *
 * Exit codes:
 *   0 — secrets generated (and written, if --write)
 *   1 — fatal error
 */

import { promises as fs } from 'node:fs';
import { existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { generateAllSecrets, SECRET_FIELDS } from './lib/env-secrets.mjs';
import { parseEnvFile, serialiseEnvFile, mergeEnv } from './lib/env-mutators.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, '..');
const ENV_LOCAL_PATH = resolve(REPO_ROOT, '.env.local');

export function parseFlags(argv) {
  const flags = { write: false, force: false, json: false, help: false };
  for (const a of argv) {
    if (a === '--write') flags.write = true;
    else if (a === '--force') flags.force = true;
    else if (a === '--json') flags.json = true;
    else if (a === '--help' || a === '-h') flags.help = true;
  }
  return flags;
}

function printHelp() {
  process.stdout.write(`generate-borjie-secrets — crypto-secret generator.

Usage:
  pnpm gen-secrets [flags]

Flags:
  --write    patch .env.local (preserves user-set values unless --force)
  --force    also overwrite already-populated values (use for rotation)
  --json     emit JSON on stdout instead of dotenv lines
  --help     show this message

What it generates:
${SECRET_FIELDS.map((s) => `  ${s.key.padEnd(28)} ${s.bytes}-byte ${s.encoding}  — ${s.purpose}`).join('\n')}

Rotation workflow (production):
  1. pnpm gen-secrets --write --force
  2. Deploy new env to a single canary instance.
  3. Once verified, deploy fleet-wide.
  4. After 24h overlap, clear *_PREVIOUS slots.
  See Docs/RUNBOOKS/encryption-at-rest-key-rotation.md.
`);
}

async function main(argv) {
  const flags = parseFlags(argv);
  if (flags.help) { printHelp(); return 0; }

  const secrets = generateAllSecrets();

  if (flags.json) {
    process.stdout.write(JSON.stringify(secrets, null, 2) + '\n');
  } else if (!flags.write) {
    process.stdout.write('# Generated by scripts/generate-borjie-secrets.mjs\n');
    process.stdout.write(`# ${new Date().toISOString()}\n\n`);
    for (const [k, v] of Object.entries(secrets)) {
      process.stdout.write(`${k}=${v}\n`);
    }
  }

  if (!flags.write) return 0;

  let existing = {};
  let existingRaw = '';
  if (existsSync(ENV_LOCAL_PATH)) {
    existingRaw = await fs.readFile(ENV_LOCAL_PATH, 'utf-8');
    existing = parseEnvFile(existingRaw);
  } else {
    process.stderr.write(`warn: ${ENV_LOCAL_PATH} not found — creating new file.\n`);
  }

  const merged = mergeEnv(existing, secrets, { onlyIfPlaceholder: !flags.force });
  const replaced = Object.keys(secrets).filter((k) => existing[k] !== merged[k]).length;
  const nextContent = serialiseEnvFile(existingRaw, merged);
  await fs.writeFile(ENV_LOCAL_PATH, nextContent, 'utf-8');
  process.stdout.write(`\n# Wrote ${ENV_LOCAL_PATH}\n`);
  process.stdout.write(`# Replaced ${replaced} secret(s); preserved ${Object.keys(secrets).length - replaced} pre-existing.\n`);
  return 0;
}

const isMain = process.argv[1] && resolve(process.argv[1]) === __filename;
if (isMain) {
  main(process.argv.slice(2)).then((code) => process.exit(code)).catch((err) => {
    process.stderr.write(`\nfatal: ${err?.stack || err}\n`);
    process.exit(1);
  });
}

export { main };
