#!/usr/bin/env node
/**
 * generate-rls-migration — emits `_rls_policies.sql` for every
 * tenant-scoped table that does NOT already have a tenant_id RLS
 * policy in the migrations tree.
 *
 * Usage:
 *   node scripts/audit/generate-rls-migration.mjs > /tmp/0179_rls_policies.sql
 *   node scripts/audit/generate-rls-migration.mjs --out=packages/database/src/migrations/0179_rls_policies.sql
 *
 * The output uses the same `current_app_tenant_id()` helper installed
 * by migration 0155 so it integrates with the existing RLS posture.
 */

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..', '..');
const MIGRATIONS_DIR = resolve(ROOT, 'packages/database/src/migrations');

const CREATE_TABLE_RE =
  /CREATE\s+TABLE\s+(?:IF\s+NOT\s+EXISTS\s+)?(?:public\.)?([a-zA-Z_][a-zA-Z0-9_]*)\s*\(([\s\S]*?)\)\s*(?:;|--)/gi;
const TENANT_COLUMN_RE =
  /^\s*(tenant_id|owner_tenant_id|installed_by_tenant_id|scope_tenant_id)\s+(TEXT|UUID|VARCHAR|CHARACTER\s+VARYING)/im;
const COVERED_TABLE_ARRAY_RE =
  /tenant_tables(?:_phase\d+)?\s+text\[\]\s*:=\s*ARRAY\s*\[([\s\S]*?)\]/gi;
const ARRAY_ITEM_RE = /'([a-zA-Z_][a-zA-Z0-9_]*)'/g;
const ALTER_RLS_RE =
  /ALTER\s+TABLE\s+(?:IF\s+EXISTS\s+)?(?:public\.)?"?([a-zA-Z_][a-zA-Z0-9_]*)"?\s+ENABLE\s+ROW\s+LEVEL\s+SECURITY/gi;
const CREATE_POLICY_RE =
  /CREATE\s+POLICY\s+\S+\s+ON\s+(?:public\.)?"?([a-zA-Z_][a-zA-Z0-9_]*)"?/gi;

function listSqlFiles(dir) {
  return readdirSync(dir).filter((e) => e.endsWith('.sql')).sort();
}

function findTenantScopedTables() {
  const tables = [];
  const seen = new Set();
  for (const f of listSqlFiles(MIGRATIONS_DIR)) {
    const body = readFileSync(join(MIGRATIONS_DIR, f), 'utf8');
    CREATE_TABLE_RE.lastIndex = 0;
    let m;
    while ((m = CREATE_TABLE_RE.exec(body)) !== null) {
      const name = (m[1] || '').toLowerCase();
      const cols = m[2] || '';
      const t = cols.match(TENANT_COLUMN_RE);
      if (!t || !name || seen.has(name)) continue;
      seen.add(name);
      tables.push({ table: name, tenantColumn: (t[1] || 'tenant_id').toLowerCase(), file: f });
    }
  }
  return tables.sort((a, b) => a.table.localeCompare(b.table));
}

function findCoveredTables() {
  const enabled = new Set();
  const withPolicy = new Set();
  for (const f of listSqlFiles(MIGRATIONS_DIR)) {
    const body = readFileSync(join(MIGRATIONS_DIR, f), 'utf8');
    ALTER_RLS_RE.lastIndex = 0;
    let m;
    while ((m = ALTER_RLS_RE.exec(body)) !== null) {
      const n = (m[1] || '').toLowerCase();
      if (n) enabled.add(n);
    }
    CREATE_POLICY_RE.lastIndex = 0;
    while ((m = CREATE_POLICY_RE.exec(body)) !== null) {
      const n = (m[1] || '').toLowerCase();
      if (n) withPolicy.add(n);
    }
    COVERED_TABLE_ARRAY_RE.lastIndex = 0;
    while ((m = COVERED_TABLE_ARRAY_RE.exec(body)) !== null) {
      const inner = m[1] || '';
      ARRAY_ITEM_RE.lastIndex = 0;
      let it;
      while ((it = ARRAY_ITEM_RE.exec(inner)) !== null) {
        const n = (it[1] || '').toLowerCase();
        if (n) { enabled.add(n); withPolicy.add(n); }
      }
    }
  }
  return { enabled, withPolicy };
}

function generate(uncoveredTables) {
  const header = `-- ============================================================================
-- Migration 0179 — RLS catch-up for every tenant-scoped table not yet
-- covered by 0155 / 0156. Generated by scripts/audit/generate-rls-migration.mjs
-- as part of the 2026-05-24 security-audit pass.
--
-- What this migration does
-- ────────────────────────
-- For every table that has a \`tenant_id\` (or scope_tenant_id /
-- installed_by_tenant_id / owner_tenant_id) column and was NOT touched
-- by an earlier RLS migration:
--
--   1. ENABLE ROW LEVEL SECURITY  (idempotent)
--   2. FORCE ROW LEVEL SECURITY   (closes the owner-bypass loophole)
--   3. CREATE POLICY tenant_isolation_select  (SELECT gated on GUC)
--   4. CREATE POLICY tenant_isolation_modify  (INSERT/UPDATE/DELETE)
--   5. CREATE POLICY service_role_bypass     (cross-tenant system jobs)
--   6. REVOKE ALL ON ... FROM anon            (no anonymous Supabase access)
--
-- Plus a global helper:
--
--   public.set_tenant_context(p_tenant_id text, p_is_service_role bool)
--
-- … which wraps the two \`set_config()\` calls the api-gateway middleware
-- already runs. Keeps the contract in ONE place so future moves are
-- auditable. \`packages/database/src/rls/with-tenant-context.ts\` calls
-- this helper indirectly via \`set_config('app.current_tenant_id', …)\`
-- so existing repository code does not need to change.
--
-- Replayable: every statement is wrapped in DROP POLICY IF EXISTS +
-- CREATE POLICY, ENABLE RLS is a one-way idempotent op, and the helper
-- function uses CREATE OR REPLACE.
-- ============================================================================

-- ----------------------------------------------------------------------------
-- 1. Session helper: set_tenant_context(tenant, service_role)
-- ----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.set_tenant_context(
  p_tenant_id text,
  p_is_service_role boolean DEFAULT FALSE
) RETURNS void
LANGUAGE plpgsql
AS $func$
BEGIN
  -- \`true\` third arg of set_config scopes the binding to the current
  -- transaction. Without it the GUC would leak across pooled requests.
  PERFORM set_config('app.current_tenant_id', p_tenant_id, true);
  PERFORM set_config('app.tenant_id', p_tenant_id, true);
  PERFORM set_config(
    'app.is_service_role',
    CASE WHEN p_is_service_role THEN 'true' ELSE 'false' END,
    true
  );
END;
$func$;

COMMENT ON FUNCTION public.set_tenant_context IS
  'Sets per-tx tenant + service-role GUCs that the RLS policies attached '
  'to every tenant-scoped table read. Called from the api-gateway '
  'tenant-context middleware and the packages/database withTenantContext() '
  'helper. SECURITY DEFINER NOT used — caller still needs USAGE on schema public.';

-- ----------------------------------------------------------------------------
-- 2. Per-table RLS install
-- ----------------------------------------------------------------------------
`;

  const lines = [header];

  for (const t of uncoveredTables) {
    const col = t.tenantColumn;
    lines.push(`
-- ---- ${t.table} (created in ${t.file}) ----
DO $do_${t.table}$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.tables
    WHERE table_schema = 'public' AND table_name = '${t.table}'
  ) THEN
    EXECUTE 'ALTER TABLE public.${t.table} ENABLE ROW LEVEL SECURITY;';
    EXECUTE 'ALTER TABLE public.${t.table} FORCE ROW LEVEL SECURITY;';

    EXECUTE 'DROP POLICY IF EXISTS tenant_isolation_select ON public.${t.table};';
    EXECUTE 'DROP POLICY IF EXISTS tenant_isolation_modify ON public.${t.table};';
    EXECUTE 'DROP POLICY IF EXISTS service_role_bypass ON public.${t.table};';

    EXECUTE 'CREATE POLICY tenant_isolation_select ON public.${t.table}
              FOR SELECT TO authenticated
              USING (${col}::text = current_setting(''app.current_tenant_id'', true));';

    EXECUTE 'CREATE POLICY tenant_isolation_modify ON public.${t.table}
              FOR ALL TO authenticated
              USING (${col}::text = current_setting(''app.current_tenant_id'', true))
              WITH CHECK (${col}::text = current_setting(''app.current_tenant_id'', true));';

    EXECUTE 'CREATE POLICY service_role_bypass ON public.${t.table}
              FOR ALL
              USING (current_setting(''app.is_service_role'', true) = ''true'')
              WITH CHECK (current_setting(''app.is_service_role'', true) = ''true'');';

    EXECUTE 'REVOKE ALL ON public.${t.table} FROM anon;';
  END IF;
END
$do_${t.table}$;
`);
  }

  lines.push(`
-- ----------------------------------------------------------------------------
-- 3. Sanity check — log every table the migration touched
-- ----------------------------------------------------------------------------
DO $$
BEGIN
  RAISE NOTICE '0179: RLS catch-up applied to ${uncoveredTables.length} tenant-scoped table(s).';
END
$$;
`);

  return lines.join('\n');
}

function main() {
  const args = process.argv.slice(2);
  const outArg = args.find((a) => a.startsWith('--out='));
  const tables = findTenantScopedTables();
  const cov = findCoveredTables();
  const uncovered = tables.filter((t) => !cov.enabled.has(t.table) || !cov.withPolicy.has(t.table));

  const sql = generate(uncovered);
  if (outArg) {
    const out = resolve(ROOT, outArg.split('=', 2)[1]);
    writeFileSync(out, sql, 'utf8');
    process.stderr.write(`wrote ${uncovered.length} table(s) to ${out}\n`);
  } else {
    process.stdout.write(sql);
    process.stderr.write(`uncovered: ${uncovered.length}\n`);
  }
}

main();
