/**
 * Persona-aware opener copy for the chat-first home tab.
 *
 * Swahili-first per the Borjie CLAUDE.md hard rule. The English line
 * sits underneath each chip so bilingual pilots can read either layer
 * without a language switch. Tap a chip → the composer auto-fills with
 * the Swahili variant and submits — the orchestrator handles language
 * routing server-side.
 *
 * The map is frozen so callers can pass tuples around without copy-on-
 * write fear.
 */
import type { Role } from '../roles/types'

