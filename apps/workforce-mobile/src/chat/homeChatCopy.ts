export function openerFor(role: Role): HomeChatOpener {
  return HOME_CHAT_OPENERS[role]
}

/**
 * Generic bilingual chat labels — reused by the composer + bubble headers.
 * Kept inline (not in the JSON catalogue) because the JSON files are owned
 * by the i18n agent this wave; we'll merge once that catalogue lands.
 *
 * The shape is a tight named record (not Record<string, …>) so direct
 * `HOME_CHAT_LABELS.errorRetry.sw` access is provably defined under
 * `noUncheckedIndexedAccess` — no `?.` ladder needed at call sites.
 */
export interface BilingualString {
  readonly sw: string
  readonly en: string
}

export interface HomeChatLabels {
  readonly composerPlaceholder: BilingualString
  readonly send: BilingualString
  readonly voice: BilingualString
  readonly attach: BilingualString
  readonly thinking: BilingualString
  readonly errorRetry: BilingualString
  readonly suggestionsTitle: BilingualString
  readonly toolCardHeader: BilingualString
  readonly proposedAction: BilingualString
  readonly riskHigh: BilingualString
  readonly riskMedium: BilingualString
  readonly riskLow: BilingualString
  readonly riskCritical: BilingualString
}

