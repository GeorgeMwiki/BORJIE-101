  send: { sw: 'Tuma', en: 'Send' },
  voice: { sw: 'Sauti', en: 'Voice' },
  attach: { sw: 'Ambatisha', en: 'Attach' },
  thinking: { sw: 'Borjie anafikiri…', en: 'Borjie is thinking…' },
